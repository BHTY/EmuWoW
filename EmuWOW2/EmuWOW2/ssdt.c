void InitSSDT() {
	HMODULE hModule;
	FARPROC EntryPoint;
	INT i;

	for (i = 0; i < sizeof(WowSystemServiceDispatchTable) / sizeof(WOW_THUNK); i++) {
		if (WowSystemServiceDispatchTable[i].EntryPoint == NULL) {
			hModule = LoadLibraryA(WowSystemServiceDispatchTable[i].ModuleName);
			EntryPoint = GetProcAddress(hModule, WowSystemServiceDispatchTable[i].ServiceName);
			WowSystemServiceDispatchTable[i].EntryPoint = EntryPoint;
		}
	}
}

DWORD LookupSSDT(LPSTR ModuleName, LPSTR ServiceName) {
	INT i;

	for (i = 0; i < sizeof(WowSystemServiceDispatchTable) / sizeof(WOW_THUNK); i++) {
		if (stricmp(ModuleName, WowSystemServiceDispatchTable[i].ModuleName) == 0) {
			if (stricmp(ServiceName, WowSystemServiceDispatchTable[i].ServiceName) == 0) {
				return i;
			}
		}
	}

	return -1;
}
